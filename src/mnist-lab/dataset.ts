// 简化的MNIST样本生成（基于程序化生成，避免大体积base64）
// 使用程序化"伪手写数字"，足够展示训练效果
export function generateMNISTDataset(): { train: MNISTSample[]; test: MNISTSample[] } {
	const train: MNISTSample[] = []
	const test: MNISTSample[] = []

	// 为每个数字生成样本
	for (let digit = 0; digit < 10; digit++) {
		// 训练集：80个/数字
		for (let i = 0; i < 80; i++) {
			train.push({
				input: generateDigitImage(digit, i),
				label: digit
			})
		}
		// 测试集：20个/数字
		for (let i = 0; i < 20; i++) {
			test.push({
				input: generateDigitImage(digit, i + 100),
				label: digit
			})
		}
	}

	// 打乱顺序
	return {
		train: shuffleArray(train),
		test: shuffleArray(test)
	}
}

// 程序化生成手写数字图像（带随机扰动）
function generateDigitImage(digit: number, seed: number): number[] {
	const image = new Array(14 * 14).fill(0)
	const rng = mulberry32(seed + digit * 1000)

	// 基础数字模板
	const basePattern = getDigitPattern(digit)

	// 随机偏移和扰动
	const offsetX = Math.floor(rng() * 3) - 1
	const offsetY = Math.floor(rng() * 3) - 1
	const _noise = rng() * 0.3

	// 应用基础模式
	for (let y = 0; y < 14; y++) {
		for (let x = 0; x < 14; x++) {
			const srcX = x - offsetX - 1
			const srcY = y - offsetY - 1
			if (srcX >= 0 && srcX < 12 && srcY >= 0 && srcY < 12) {
				const value = basePattern[srcY * 12 + srcX]
				// 添加随机噪声和粗细变化
				let pixel = value * (0.7 + rng() * 0.5)
				// 偶尔添加额外噪点
				if (rng() < 0.05) pixel = rng() * 0.5
				image[y * 14 + x] = Math.min(1, Math.max(0, pixel))
			} else if (rng() < 0.02) {
				// 边缘随机噪点
				image[y * 14 + x] = rng() * 0.2
			}
		}
	}

	return image
}

